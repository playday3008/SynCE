<p>Dynamite is a tool and library for decompressing data compressed with <a
href="http://www.pkware.com/products/developers/dcl/">PKWARE Data Compression
Library</a> and it was created from the specification provided by <a
href="http://groups.google.com/groups?q=author:Ben+author:Rudiak-Gould&hl=en&rnum=1&selm=34259d52.0108131038.21dd88f9%40posting.google.com">a
post in the comp.compression newsgroup</a>.</p>

<p>The dynamite library is needed to make the <a href="orange.php">Orange</a>
tool capable of squeezing out juicy .cab files from self-extracting
installation programs created by the <a href="http://www.indigorose.com">Setup
Factory</a> installation program.</p>

