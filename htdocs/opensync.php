<h1>SynCE and OpenSync</h1>

<p><i>by <a href="mailto:twogood@users.sourceforge.net">David Eriksson</a>,
developer of the core SynCE modules.</i></p>

<p>Lately there has been a number of enquires about a SynCE plugin for <a
href="http://opensync.org">OpenSync</a>. I am very happy that people express
their interest in such a plugin.</p>

<p>As the author of the SynCE plugin for <a
href="http://www.multisync.org/">MultiSync</a>, am I porting that plugin to
OpenSync? No, I'm not.</p>

<p>Instead I am currently working on a project called <b>MidaSync</b>. It will
include an OpenSync plugin for synchronization with Windows CE devices.</p>

<p>MidaSync is <b>not</b> part of the SynCE project and there is no public
source code repository or mailing list available.</p>

<p>When MidaSync comes closer to release, the <a
href="http://sourceforge.net/project/project_donations.php?group_id=30550">SynCE
donors</a> will probably be offered to do beta testing.</p>

