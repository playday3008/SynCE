<H1>Step 1 &ndash; Installing SynCE for KDE from binary packages</H1>
<P>There are two possibilities for finding SynCE binary packages. The first one is to use those coming with your distribution. Then, you might also check the SynCE project <A HREF="http://sourceforge.net/project/showfiles.php?group_id=30550">download
page</A> to find a possibly newer version (in particular, thanks to Martin Lipocky a new package is available for Suse 10.0). Please make sure to use the latest version.</P>
<P>Install then the packages according to your distribution
specificities. We cite here two examples</P>
<UL>
	<LI><P>Under Debian, simply issue the command</P>
</UL>
<OL>
	<P><FONT FACE="Courier 10 Pitch">apt-get synce-kde synce-serial</FONT></P>
</OL>
<UL>
	<LI><P>If you are running Suse 10.0,
	make sure you downloaded the new package synce-kde-pack from the
	<A HREF="http://sourceforge.net/project/showfiles.php?group_id=30550">download
	page</A>  (the original packages suffer from a bug) and install it
	with Yast (use for example the context menu in Konqueror).</P>
</UL>
<P>Once this step is over, it is strongly advisable to follow the
second step of the tutorial : <A HREF="synce-kde-basic-configuration.php">basic
configuration of Synce for KDE</A>.</P>
